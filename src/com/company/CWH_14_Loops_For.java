package com.company;

public class CWH_14_Loops_For {
    public static void main(String[] args) {


        //     1
        for ( int i=1;i<=10;i++){
            System.out.println(i);
        }


        //     2


        int n=5;
        for ( int i=0;i<n;i++){
            System.out.println(i*2 +1);
        }



        //     3

        for (int i=7;i>0;i--){
            System.out.println(i);
        }


        //     4

        for (int i=7;i!=0;i--){
            System.out.println(i);
        }




        //    5
        int m=100;
        for (int i=m;i>0;i--){
            System.out.println(i);
        }


    }
}
