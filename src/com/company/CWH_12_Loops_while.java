public class CWH_12_Loops_while {
    public static void main(String[] args) {
        int  a=1;
        while (a<=3) {
            System.out.println(a);
            a++;
        }


        int b=100;
        while (b<=200){
            System.out.println(b);
            b++;
        }


    }
}
